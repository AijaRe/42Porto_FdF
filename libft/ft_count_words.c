#include "libft.h"

int	ft_count_words(char *str)
{
	int	a;
	int	i;

	a = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == 32)
		{
			i++;
		}
		if (str[i] != 32 && str[i] != '\0')
		{
			a++;
			i++;
		}
		while (str[i] != 32 && str[i] != '\0')
		{
			i++;
		}
	}
	return (a);
}
